    // Method to load persons from the database
   public void loadPersons(String category, String searchQuery) {
    try {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear table

        Connection con = DBconnexion.ConnectDB();
        String sql = "SELECT id, name, email, phone, category FROM bioperson WHERE 1=1";  // Base query

        // Add category filter if not "All"
        if (!category.equals("All")) {
            sql += " AND category = ?";  // Add category condition
        }

        // Add search query for name filter
        if (!searchQuery.isEmpty()) {
            sql += " AND LOWER(name) LIKE ?";  // Add name condition (case-insensitive)
        }

        // Print out the SQL query for debugging
        System.out.println("SQL Query: " + sql);

        PreparedStatement ps = con.prepareStatement(sql);

        int index = 1;

        // Set the category parameter if not "All"
        if (!category.equals("All")) {
            ps.setString(index++, category);
        }

        // Set the search query parameter if applicable
        if (!searchQuery.isEmpty()) {
            ps.setString(index, "%" + searchQuery.toLowerCase() + "%");
        }

        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),  // ID
                rs.getString("name"),
                rs.getString("email"),
                rs.getString("phone"),
                rs.getString("category")
            });
        }

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading data.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}



