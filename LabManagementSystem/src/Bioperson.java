    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
      int selectedRow = jTable1.getSelectedRow(); // Get selected row
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a row to delete.");
        return;
    }

    // Confirm deletion
    int confirm = JOptionPane.showConfirmDialog(
        this, 
        "Are you sure you want to delete this record?", 
        "Confirm Deletion", 
        JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) {
        return; // If user cancels
    }

    try {
        // Get the selected person's ID
        int personId = (int) jTable1.getValueAt(selectedRow, 0); // Assuming ID is in column 0

        // Delete from the database
        Connection conn = DBconnexion.ConnectDB();
        String sql = "DELETE FROM bioperson WHERE id = ?";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setInt(1, personId);

        int rowsAffected = stmt.executeUpdate();
        if (rowsAffected > 0) {
            // Remove the row from the table
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.removeRow(selectedRow);
            JOptionPane.showMessageDialog(this, "Record deleted successfully.");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete the record.");
        }

        stmt.close();
        conn.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
  // TODO add your handling code here:
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
       int selectedRow = jTable1.getSelectedRow(); // Get selected row index
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a person to edit.", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Retrieve data from the selected row
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    selectedPersonId = (int) model.getValueAt(selectedRow, 0); // Get ID from column 0
    String name = (String) model.getValueAt(selectedRow, 1);  // Get Name from column 1
    String email = (String) model.getValueAt(selectedRow, 2); // Get Email from column 2
    String phone = (String) model.getValueAt(selectedRow, 3); // Get Phone from column 3
    String category = (String) model.getValueAt(selectedRow, 4); // Get Category from column 4

    // Populate the fields with the selected row's data
    nameField.setText(name);
    emailField.setText(email);
    phoneField.setText(phone);
    jComboBox1.setSelectedItem(category);

    // Set editing mode
    isEditing = true;

    // Make the form panel visible if hidden
    jPanel2.setVisible(true); // Replace `formPanel` with the actual variable name of your panel

    }//GEN-LAST:event_editButtonActionPerformed

