    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        String name = nameField.getText();
        String email = emailField.getText();
        String phone = phoneField.getText();
        String category = jComboBox1.getSelectedItem().toString();

        if (name.isEmpty() || email.isEmpty() || phone.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            Connection con = DBconnexion.ConnectDB();
            if (isEditing) {
                // Update the existing record
                String sql = "UPDATE bioperson SET name = ?, email = ?, phone = ?, category = ? WHERE id = ?";
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, name);
                ps.setString(2, email);
                ps.setString(3, phone);
                ps.setString(4, category);
                ps.setInt(5, selectedPersonId);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Person updated successfully.");
            } else {
                // Insert a new record
                String sql = "INSERT INTO bioperson (name, email, phone, category) VALUES (?, ?, ?, ?)";
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, name);
                ps.setString(2, email);
                ps.setString(3, phone);
                ps.setString(4, category);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Person added successfully.");
            }

            // Reset the form
            nameField.setText("");
            emailField.setText("");
            phoneField.setText("");
            jComboBox1.setSelectedIndex(0);
            isEditing = false; // Reset editing mode
            selectedPersonId = -1; // Reset selected ID
            loadPersons("All","" ); // Refresh table
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error while saving data.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        jPanel2.setVisible(false);
    }//GEN-LAST:event_submitButtonActionPerformed

