    private void submitButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButton2ActionPerformed
       
       String groupName = nameField2.getText();
    String manager = emailField2.getText();
    String creationDate = phoneField2.getText();

    // Validate input fields
    if (groupName.isEmpty() || manager.isEmpty() || creationDate.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        // Establish database connection
        Connection con = DBconnexion.ConnectDB();

        if (isEditingGroup) {
            // Update the existing group record
            String sql = "UPDATE stgroup SET name = ?, manager = ?, creation_date = ? WHERE id = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, groupName);
            ps.setString(2, manager);
            ps.setString(3, creationDate);
            ps.setInt(4, selectedGroupId); // Use selected group ID for identification
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Group updated successfully.");
        } else {
            // Insert a new group record
            String sql = "INSERT INTO stgroup (name, manager, creation_date) VALUES (?, ?, ?)";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, groupName);
            ps.setString(2, manager);
            ps.setString(3, creationDate);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Group added successfully.");
        }

        // Reset the form
        nameField2.setText("");
        emailField2.setText("");
        phoneField2.setText("");
        isEditingGroup = false; // Reset editing mode
        selectedGroupId = -1; // Reset selected group ID

        // Refresh the groups table
        loadGroups("");
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error while saving data.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    // Hide the form panel
    jPanel4.setVisible(false);


    }//GEN-LAST:event_submitButton2ActionPerformed

    private void deleteButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButton2ActionPerformed
int selectedRow = groupsTable.getSelectedRow(); // Get selected row index
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a group to delete.", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Confirm deletion
    int confirm = JOptionPane.showConfirmDialog(
        this,
        "Are you sure you want to delete this group?",
        "Confirm Deletion",
        JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) {
        return; // If user cancels
    }

    try {
        // Get the selected group's ID (assuming ID is in column 0)
        int groupId = (int) groupsTable.getValueAt(selectedRow, 0);

        // Delete from the database
        Connection conn = DBconnexion.ConnectDB();
        String sql = "DELETE FROM stgroup WHERE id = ?";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setInt(1, groupId);

        int rowsAffected = stmt.executeUpdate();
        if (rowsAffected > 0) {
            // Remove the row from the table
            DefaultTableModel model = (DefaultTableModel) groupsTable.getModel();
            model.removeRow(selectedRow);
            JOptionPane.showMessageDialog(this, "Group deleted successfully.");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete the group.");
        }

        stmt.close();
        conn.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

  // TO     
    }//GEN-LAST:event_deleteButton2ActionPerformed

