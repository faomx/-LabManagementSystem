    public void loadGroups(String searchQuery) {
    try {
        // Get the table model
        DefaultTableModel model = (DefaultTableModel) groupsTable.getModel();
        model.setRowCount(0); // Clear existing rows

        // Connect to the database
        Connection con = DBconnexion.ConnectDB();

        // Modify the SQL to filter based on the search query
        String sql = "SELECT id, name, manager, creation_date FROM stgroup WHERE name LIKE ?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, "%" + searchQuery + "%");  // Add wildcard for partial matches
        ResultSet rs = ps.executeQuery();

        // Add rows to the table
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("name"),
                rs.getString("manager"),
                rs.getString("creation_date")
            });
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading groups.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
   

