    private int groupId;

public void setGroupId(int groupId) {
    this.groupId = groupId;  // Store the passed groupId
    loadAchievements();       // Call the method to load achievements when groupId is set
}
private void loadAchievements() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Clear the table

    // Check if groupId is valid
    if (this.groupId == 0) {
        JOptionPane.showMessageDialog(this, "Group ID is not set or invalid.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try (Connection con = DBconnexion.ConnectDB()) {
        // SQL query to get achievements based on groupId
        String sql = "SELECT id, achievement_name, achievement_date, description FROM csgroup_achievements WHERE group_id = ?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setInt(1, this.groupId);  // Set groupId in the query

        ResultSet rs = ps.executeQuery();

        // Loop through the result set and add rows to the table
        while (rs.next()) {
            int achievementId = rs.getInt("id");
            String name = rs.getString("achievement_name");
            String date = rs.getString("achievement_date");
            String description = rs.getString("description");

            model.addRow(new Object[]{achievementId, name, date, description}); // Update column mappings
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading achievements.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}


