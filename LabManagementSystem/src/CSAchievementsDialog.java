    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int selectedRow = jTable1.getSelectedRow(); // Get selected row index

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select an achievement to remove.", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Retrieve the achievement ID from the selected row
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    int achievementId = (int) model.getValueAt(selectedRow, 0);

    // Confirm removal
    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to remove this achievement?", "Confirm", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        try (Connection con = DBconnexion.ConnectDB()) {
            String sql = "DELETE FROM csgroup_achievements WHERE id = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, achievementId);
            ps.executeUpdate();

            // Remove the row from the table
            model.removeRow(selectedRow);

            JOptionPane.showMessageDialog(this, "Achievement removed successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error removing achievement.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

