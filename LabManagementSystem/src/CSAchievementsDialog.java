    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String achievementName = txtAchievementName.getText();
    String achievementDate = txtAchievementDate.getText();
    String description = txtDescription.getText(); // Use 'description' instead of 'status'

    // Validate fields
    if (achievementName.isEmpty() || achievementDate.isEmpty() || description.isEmpty()) {
        JOptionPane.showMessageDialog(this, "All fields are required.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try (Connection con = DBconnexion.ConnectDB()) {
        String sql = "INSERT INTO csgroup_achievements (group_id, achievement_name, achievement_date, description) VALUES (?, ?, ?, ?)";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setInt(1, groupId);  // Pass the groupId
        ps.setString(2, achievementName);
        ps.setString(3, achievementDate);
        ps.setString(4, description);  // Insert into the 'description' column
        ps.executeUpdate();

        // Clear text fields after adding
        txtAchievementName.setText("");
        txtAchievementDate.setText("");
        txtDescription.setText("");  // Clear the 'description' field

        // Refresh the table to show the new achievement
        loadAchievements();
        jPanel1.setVisible(false);
       

        JOptionPane.showMessageDialog(this, "Achievement added successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error adding achievement.", "Error", JOptionPane.ERROR_MESSAGE);
    
    }// TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

