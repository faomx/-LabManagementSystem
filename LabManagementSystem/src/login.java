    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword()); // Convert char[] to String

        // Check the credentials in the database
        boolean isValidUser = DBconnexion.checkUserCredentials(username, password);

        // If login is successful, open the Taskbar screen and close the login screen
        if (isValidUser) {
            JOptionPane.showMessageDialog(this, "Login Successful!");

            // Open Taskbar JFrame
            Dashboard taskbarScreen = new Dashboard();
            taskbarScreen.setVisible(true);

            // Close the current login JFrame
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Invalid credentials, please try again.");
        }  

    }//GEN-LAST:event_jButton1ActionPerformed

